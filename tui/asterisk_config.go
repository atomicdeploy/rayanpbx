// CommitConfigChange commits changes to the Asterisk configuration Git repository
// This creates a snapshot of the configuration for version control and rollback capability
func (acm *AsteriskConfigManager) CommitConfigChange(action, description string) error {
	green := color.New(color.FgGreen)
	yellow := color.New(color.FgYellow)

	// Check if /etc/asterisk is a Git repository
	asteriskDir := "/etc/asterisk"
	gitDir := asteriskDir + "/.git"

	if _, err := os.Stat(gitDir); os.IsNotExist(err) {
		if acm.verbose {
			yellow.Println("‚ö†Ô∏è  /etc/asterisk is not a Git repository, skipping commit")
		}
		return nil // Not an error - just skip if not a git repo
	}

	// Try to use the helper script first
	scriptPaths := []string{
		"/opt/rayanpbx/scripts/asterisk-git-commit.sh",
		"/usr/local/bin/asterisk-git-commit.sh",
	}

	var scriptPath string
	for _, path := range scriptPaths {
		if _, err := os.Stat(path); err == nil {
			scriptPath = path
			break
		}
	}

	if scriptPath != "" {
		// Use the helper script
		cmd := exec.Command(scriptPath, "commit", action, description)
		cmd.Env = append(os.Environ(), "SOURCE=TUI", "RAYANPBX_TUI=1")
		output, err := cmd.CombinedOutput()
		if err != nil {
			if acm.verbose {
				yellow.Printf("‚ö†Ô∏è  Git commit helper failed: %v\n", err)
				yellow.Printf("   Output: %s\n", string(output))
			}
			// Fall through to inline git commit
		} else {
			if acm.verbose {
				green.Printf("‚úÖ Configuration snapshot saved\n")
			}
			return nil
		}
	}

	// Fallback: Inline git commit
	// Change to asterisk directory
	originalDir, err := os.Getwd()
	if err != nil {
		originalDir = "" // Mark as invalid
	}
	if err := os.Chdir(asteriskDir); err != nil {
		return fmt.Errorf("failed to change to asterisk directory: %v", err)
	}
	defer func() {
		if originalDir != "" {
			_ = os.Chdir(originalDir) // Best effort restore
		}
	}()

	// Check if there are changes to commit
	statusCmd := exec.Command("git", "status", "--porcelain")
	statusOutput, err := statusCmd.Output()
	if err != nil {
		return fmt.Errorf("git status failed: %v", err)
	}
	
	if len(strings.TrimSpace(string(statusOutput))) == 0 {
		if acm.verbose {
			yellow.Println("‚ö†Ô∏è  No changes to commit")
		}
		return nil
	}

	// Stage all changes
	addCmd := exec.Command("git", "add", "-A")
	if err := addCmd.Run(); err != nil {
		return fmt.Errorf("failed to stage changes: %v", err)
	}

	// Build commit message
	commitMsg := fmt.Sprintf("[%s] %s\n\nSource: TUI\nTimestamp: %s",
		action, description, getTimestamp())

	// Commit
	commitCmd := exec.Command("git", "commit", "-m", commitMsg)
	if output, err := commitCmd.CombinedOutput(); err != nil {
		return fmt.Errorf("git commit failed: %v (output: %s)", err, string(output))
	}

	// Verify the commit was successful - check if repository is still dirty
	statusCmd = exec.Command("git", "status", "--porcelain")
	statusOutput, err = statusCmd.Output()
	if err != nil {
		yellow.Printf("‚ö†Ô∏è  Warning: Could not verify commit status: %v\n", err)
	} else if len(strings.TrimSpace(string(statusOutput))) > 0 {
		// Repository is still dirty after commit - this is a problem
		yellow.Println("‚ö†Ô∏è  Warning: Repository still has uncommitted changes after commit")
		yellow.Printf("   Uncommitted files:\n%s", string(statusOutput))
		return fmt.Errorf("repository still dirty after commit - some files may not have been committed")
	}

	if acm.verbose {
		green.Println("‚úÖ Configuration snapshot saved")
	}

	return nil
}

// GetAsteriskGitStatus checks the Git status of /etc/asterisk and returns status information
// Returns: isDirty (bool), statusMessage (string), error
func (acm *AsteriskConfigManager) GetAsteriskGitStatus() (bool, string, error) {
	asteriskDir := "/etc/asterisk"
	gitDir := asteriskDir + "/.git"

	// Check if /etc/asterisk is a Git repository
	if _, err := os.Stat(gitDir); os.IsNotExist(err) {
		return false, "Not a Git repository", nil
	}

	// Change to asterisk directory
	originalDir, err := os.Getwd()
	if err != nil {
		originalDir = ""
	}
	if err := os.Chdir(asteriskDir); err != nil {
		return false, "", fmt.Errorf("failed to change to asterisk directory: %v", err)
	}
	defer func() {
		if originalDir != "" {
			_ = os.Chdir(originalDir)
		}
	}()

	// Get status
	statusCmd := exec.Command("git", "status", "--porcelain")
	statusOutput, err := statusCmd.Output()
	if err != nil {
		return false, "", fmt.Errorf("git status failed: %v", err)
	}

	status := strings.TrimSpace(string(statusOutput))
	if len(status) == 0 {
		return false, "Clean (all changes committed)", nil
	}

	// Count uncommitted files
	lines := strings.Split(status, "\n")
	return true, fmt.Sprintf("Dirty (%d uncommitted files)", len(lines)), nil
}

// IsAsteriskRepoDirty returns true if /etc/asterisk has uncommitted changes
func (acm *AsteriskConfigManager) IsAsteriskRepoDirty() bool {
	isDirty, _, _ := acm.GetAsteriskGitStatus()
	return isDirty
}

